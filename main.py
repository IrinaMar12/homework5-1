example='happiness'
print(example[0])            #-первый символ строки принят за '0'
print(example[-1])        # -отрицательный индекс берет отсчет с конца строки.
print(example[4:])        #-срез с четвертого элемента и до конца.
print(example[::-1])      #-без указания начала и конца с отрицательным индексом.
print(example[1::2])      #- выведен каждый второй символ.